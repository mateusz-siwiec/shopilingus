package com.company;

public class Product {
  private double price;
  private String type;
  private String name;
  private String size;
  private double weight;
  private double tax;

    public Product(double price, String type, String name, String size, double weight, double tax) {
        this.price = price;
        this.type = type;
        this.name = name;
        this.size = size;
        this.weight = weight;
        this.tax = tax;
    }


}
